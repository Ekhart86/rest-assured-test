package model.api.profile;

public class Result {

    private int user_id;
    private String first_name;
    private String last_name;
    private String email;

    public int getUser_id() {
        return user_id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public String getEmail() {
        return email;
    }
}
